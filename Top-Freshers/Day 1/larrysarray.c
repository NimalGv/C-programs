char* larrysArray(int size, int* array) {
    int count=0;
    for(int i=0; i<size-1; i++){
        for(int j=i+1; j<size; j++){
            if(array[i]>array[j])
                count++;
        }
    }
    if(count%2==0)
        return "YES";
    return "NO";

}

